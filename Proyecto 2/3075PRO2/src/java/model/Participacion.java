public class Participacion {
    
    private int cod_participacion;
    private int cod_medico;
    private int cod_congreso;
    private int cod_hospital;

    public int getCod_participacion() {
        return cod_participacion;
    }

    public void setCod_participacion(int cod_participacion) {
        this.cod_participacion = cod_participacion;
    }

    public int getCod_medico() {
        return cod_medico;
    }

    public void setCod_medico(int cod_medico) {
        this.cod_medico = cod_medico;
    }

    public int getCod_congreso() {
        return cod_congreso;
    }

    public void setCod_congreso(int cod_congreso) {
        this.cod_congreso = cod_congreso;
    }

    public int getCod_hospital() {
        return cod_hospital;
    }

    public void setCod_hospital(int cod_hospital) {
        this.cod_hospital = cod_hospital;
    }
    
    
    
}
